package org.example;

public class Calculator {
    double Add(double a, double b) {
        return a + b;
    }

    int Add(int a, int b) {
        return a + b;
    }

    double Subtract(double a, double b) {
        return a - b;
    }

    int Subtract(int a, int b) {
        return a - b;
    }
}
